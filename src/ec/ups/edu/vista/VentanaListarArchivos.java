    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        this.evt = evt;
        if (! txtRuta.getText().equals("")) {
            File ruta = new File(txtRuta.getText());
            File[] archivos = ruta.listFiles();
            DefaultListModel model = new DefaultListModel();
            for (File archivo : archivos) {
                if (archivo.isDirectory()) {
                    model.addElement(archivo.getName());
                }
            }
            jListDirectorio.setModel(model);
            model = new DefaultListModel();
            for (File archivo : archivos) {
                if (archivo.isFile() && !archivo.isHidden()) {
                    model.addElement(archivo.getName());
                }
            }
            jListArchivos.setModel(model);
            model = new DefaultListModel();
            for (File archivo : archivos) {
                if (archivo.isHidden()) {
                    model.addElement(archivo.getName());
                }
            }
            jListOcultos.setModel(model);
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese una ruta");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void jListDirectorioValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListDirectorioValueChanged
        controlador = 1;
        String elementoSelecionado = (String) jListDirectorio.getSelectedValue();
        String rutaselecionada = txtRuta.getText().trim() + "\\" + elementoSelecionado;
        rutaDirectorio = rutaselecionada;
        File fichero = new File(rutaselecionada);
        long fechaMilisegundos = fichero.lastModified();
        Date fecha = new Date(fechaMilisegundos);
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jLModificacion.setText(formato.format(fecha));
        jLRuta.setText(fichero.getAbsolutePath());
        jLTamaño.setText("" + fichero.length() / 1024);
        jListArchivos.clearSelection();
        jListOcultos.clearSelection();
    }//GEN-LAST:event_jListDirectorioValueChanged

