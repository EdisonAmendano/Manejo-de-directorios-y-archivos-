    private void jmiCrearArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiCrearArchivoActionPerformed
        String nombre = JOptionPane.showInputDialog("Crear Fichero");
        String ruta;
        if (controlador == 0) {
            ruta = txtRuta.getText() + "\\" + nombre;

        } else {
            ruta = rutaDirectorio + "\\" + nombre;
        }
        File archivoNuevo = new File(ruta);
        if (!archivoNuevo.exists()) {
            try {
                archivoNuevo.createNewFile();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "no se pudo crar el archivo");
            }
        } else {
            JOptionPane.showMessageDialog(this, "El archivo ya existe");
        }
        btnBuscarActionPerformed(evt);
    }//GEN-LAST:event_jmiCrearArchivoActionPerformed

    private void jmiRenombrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiRenombrarActionPerformed
        String archivoSelecionado = null;
        if (!jListDirectorio.isSelectionEmpty()) {
            archivoSelecionado = (String) jListDirectorio.getSelectedValue();
        } else if (!jListArchivos.isSelectionEmpty()) {
            archivoSelecionado = (String) jListArchivos.getSelectedValue();
        } else if (!jListOcultos.isSelectionEmpty()) {
            archivoSelecionado = (String) jListOcultos.getSelectedValue();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione");
        }
        String rutaArchivoSelecionado = txtRuta.getText().trim() + "\\" + archivoSelecionado;
        File archivo = new File(rutaArchivoSelecionado);

        String nombreNuevoArchivo = JOptionPane.showInputDialog("Ingresa el nuevo nombre del archivo");
        String rutaNuevoArchivo = txtRuta.getText().trim() + "\\" + nombreNuevoArchivo;
        File archivoNuevo = new File(rutaNuevoArchivo);

        if (!archivoNuevo.exists()) {
            archivo.renameTo(archivoNuevo);
        } else {
            JOptionPane.showMessageDialog(this, "Ya existe un archivo con ese nombre");
        }
        btnBuscarActionPerformed(evt);
    }//GEN-LAST:event_jmiRenombrarActionPerformed

